function setupPlotLab(obj)
    obj.plotlabOBJ = plotlab();
    obj.plotlabOBJ.applyRecipe(...
            'colorOrder', [1 0 0; 0 0 1], ...
            'axesBox', 'off', ...
            'axesTickDir', 'in', ...
            'renderer', 'painters', ...
            'axesTickLength', [0.01 0.01], ...
            'legendLocation', 'SouthWest', ...
            'figureWidthInches', 20.0000, ...
            'figureHeightInches', 10.1354);
end 